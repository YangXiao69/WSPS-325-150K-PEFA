/** 
	*	SPI速度设置函数
  * SPI速度=fAPB1/分频系数
  * @ref SPI_BaudRate_Prescaler:SPI_BAUDRATEPRESCALER_2~SPI_BAUDRATEPRESCALER_2 256
  * fAPB1时钟一般为42Mhz：
	*/
void SPI2_SetSpeed(uint8_t SPI_BaudRatePrescaler)
{
    assert_param(IS_SPI_BAUDRATE_PRESCALER(SPI_BaudRatePrescaler));//判断有效性
    __HAL_SPI_DISABLE(&hspi2);            //关闭SPI
    hspi2.Instance->CR1&=0XFFC7;          //位3-5清零，用来设置波特率
    hspi2.Instance->CR1|=SPI_BaudRatePrescaler;//设置SPI速度
    __HAL_SPI_ENABLE(&hspi2);             //使能SPI
    
}


/**
  * 函数功能: 往串行Flash读取写入一个字节数据并接收一个字节数据
  * 输入参数: byte：待发送数据
  * 返 回 值: uint8_t：接收到的数据
  * 说    明：无
  */
uint8_t SPIx_ReadWriteByte(SPI_HandleTypeDef* hspi,uint8_t byte)
{
  uint8_t d_read,d_send=byte;
  if(HAL_SPI_TransmitReceive(hspi,&d_send,&d_read,1,0xFF)!=HAL_OK)
  {
    d_read=0xFF;
  }
  return d_read; 
}



uint8_t SPI2_SendByte(uint8_t byte)
{
	uint8_t status = 0;
	uint8_t SPI2TxBuf[1] = {0};
	uint8_t SPI2RxBuf[1] = {0};	
	
	uint8_t retval = 0;
	
	SPI2TxBuf[0] = byte;
	
	status = HAL_SPI_TransmitReceive(&hspi2, (uint8_t*)SPI2TxBuf, (uint8_t *)SPI2RxBuf, 1, 1000000);
	
	if( status!= HAL_OK)	
	{
		_Error_Handler(__FILE__, __LINE__);
	}
	
	retval = SPI2RxBuf[0];
	
	return retval;
}

