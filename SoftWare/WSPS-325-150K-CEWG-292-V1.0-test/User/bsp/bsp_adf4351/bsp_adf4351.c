uint16_t PLL_NSS  = GPIO_Pin_0 ;
uint16_t PLL_DATA = GPIO_Pin_12;
uint16_t PLL_LE   = GPIO_Pin_11;


void ADF4351_GPIO_Init(void)
{    
    GPIO_InitTypeDef GPIO_InitStructure                 ;                                                                                            
    RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOC, ENABLE);   
    RCC_AHB1PeriphClockCmd(RCC_AHB1Periph_GPIOD, ENABLE); 
    
    GPIO_InitStructure.GPIO_Pin   = GPIO_Pin_10 | GPIO_Pin_11| GPIO_Pin_12;	
    GPIO_InitStructure.GPIO_Mode  = GPIO_Mode_OUT   ;    
    GPIO_InitStructure.GPIO_OType = GPIO_OType_PP   ;
    GPIO_InitStructure.GPIO_PuPd  = GPIO_PuPd_UP    ;
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_25MHz;                         
    GPIO_Init(GPIOC, &GPIO_InitStructure)           ;  
    
    GPIO_InitStructure.GPIO_Pin   = GPIO_Pin_0      ;	
    GPIO_InitStructure.GPIO_Mode  = GPIO_Mode_OUT   ;    
    GPIO_InitStructure.GPIO_OType = GPIO_OType_PP   ;
    GPIO_InitStructure.GPIO_PuPd  = GPIO_PuPd_UP    ;
    GPIO_InitStructure.GPIO_Speed = GPIO_Speed_25MHz;                         
    GPIO_Init(GPIOD, &GPIO_InitStructure)           ;
    
    GPIO_SetBits  (GPIOD, PLL_NSS );//Ê¹ÄÜÆ÷¼þADF4351
    GPIO_ResetBits(GPIOC, PLL_LE  ); 
    GPIO_ResetBits(GPIOC, PLL_DATA);
    GPIO_ResetBits(GPIOC, PLL_CLK);  
}

void adf4351_write_data(unsigned int register_data)
{
	unsigned char i = 0;
	GPIO_ResetBits(GPIOC, PLL_LE) ;  
	delay_us(4)                   ;             
	GPIO_ResetBits(GPIOC, PLL_CLK);
	delay_us(4)                   ;                         
	for(i=0;i<=31;i++)
    { 
        GPIO_ResetBits(GPIOC, PLL_CLK);
        delay_us(2)                   ;
        if(0x80000000 == (register_data&0x80000000))
        {
        	GPIO_SetBits(GPIOC, PLL_DATA);
        }
        else 
        {
        	GPIO_ResetBits(GPIOC, PLL_DATA);
        }
        GPIO_ResetBits(GPIOC, PLL_CLK)  ;
        delay_us(2)                     ;
        GPIO_SetBits(GPIOC, PLL_CLK)    ;
        delay_us(2)                     ;
        GPIO_ResetBits(GPIOC, PLL_CLK)  ; 
        delay_us(2)                     ;
        register_data=register_data << 1;
        delay_us(2)                     ;
    }
    GPIO_ResetBits(GPIOC, PLL_LE) ;  
	delay_us(4)                   ;             
	GPIO_SetBits(GPIOC, PLL_LE)   ;
	delay_us(4)                   ;
	GPIO_ResetBits(GPIOC, PLL_LE) ;
	delay_us(4)                   ;  
	GPIO_ResetBits(GPIOC, PLL_CLK);  	 	 
}


